   				<strong>Статья 39<sup>7</sup>. Договор страхования ответственности члена саморегулируемой организации в области пожарной безопасности</strong>
    <br />
   				
    <br />
   				Объектом страхования по договору страхования ответственности 				члена саморегулируемой организации в области пожарной 				безопасности (далее - договор страхования ответственности) 				являются имущественные интересы, связанные с риском 				ответственности члена саморегулируемых организаций в области 				пожарной безопасности (страхователя) по обязательствам, 				возникающим вследствие причинения ущерба заказчику, заключившему 				договор на выполнение работ и оказание услуги в области пожарной 				безопасности, и (или) третьим лицам.
    <br />
   				
    <br />
   				Страховым случаем по договору страхования ответственности 				является установленный вступившим в законную силу решением 				арбитражного суда или признанный страховщиком факт причинения 				ущерба действиями (бездействием) члена саморегулируемой 				организации в области пожарной безопасности в результате 				нарушения требований федеральных стандартов выполнения работ и 				оказания услуг в области пожарной безопасности и (или) 				стандартов саморегулируемой организации, членом которой являлся 				страхователь на момент причинения ущерба.
    <br />
   				
    <br />
   				В случае наступления страхового случая страховщик производит 				страховую выплату в размере причиненного заказчику и (или) 				третьему лицу реального ущерба, установленном вступившим в 				законную силу решением арбитражного суда, но не более чем в 				размере страховой суммы по договору страхования ответственности.
    <br />
   				
    <br />
   				Договор страхования ответственности заключается на срок не менее 				одного года с условием возмещения ущерба, причиненного в период 				действия договора страхования ответственности, в течение срока 				исковой давности, установленного законодательством Российской 				Федерации.
    <br />
   				
    <br />
   				Страховой тариф по договору страхования ответственности может 				определяться страховщиками в зависимости от количества 				предыдущих страховых случаев и иных влияющих на степень риска 				причинения ущерба обстоятельств.
    <br />
   				
    <br />
   				Договор страхования ответственности может предусматривать уплату 				членом саморегулируемой организации в области пожарной 				безопасности страховой премии в рассрочку и сроки уплаты 				страховых взносов. 
    <br />
   				
    <br />
   				Датой уплаты страховой премии (страхового взноса) считается день 				уплаты страховой премии (страхового взноса) наличными деньгами 				страховщику или день перечисления страховой премии (страхового 				взноса) на расчетный счет страховщика.
    <br />
   				
    <br />
   				Договор страхования ответственности вступает в силу с момента 				уплаты страхователем первого страхового взноса, если 				законодательством Российской Федерации не предусмотрено иное.
    <br />
   				
    <br />
   				Контроль за осуществлением своими членами страхования 				ответственности проводится саморегулируемой организацией, 				которая вправе устанавливать дополнительные не противоречащие 				законодательству Российской Федерации требования к договорам 				страхования ответственности, заключаемым членами такой 				саморегулируемой организации.
    <br />
   				
    <br />
   				<strong>Статья 39<sup>8</sup>. Национальное объединение саморегулируемых 				организаций в области пожарной безопасности</strong>
    <br />
   				
    <br />
   				Саморегулируемые организации в области пожарной безопасности 				вправе создавать объединения саморегулируемых организаций и быть 				их членами.
    <br />
   				
    <br />
   				Объединение саморегулируемых организаций, в составе которого 				объединились более чем пятьдесят процентов всех саморегулируемых 				организаций в области пожарной безопасности, сведения о которых 				включены в государственный реестр саморегулируемых организаций в 				области пожарной безопасности, в целях координации своей 				деятельности, выработки единых правил и стандартов выполнения 				работ и оказания услуг в области пожарной безопасности, 				обеспечения взаимодействия с органами государственной власти по 				вопросам выработки государственной политики в области пожарной 				безопасности, а также с потребительским сообществом вправе 				приобрести статус национального объединения саморегулируемых 				организаций в области пожарной безопасности в порядке, 				установленном настоящим Федеральным законом.
    <br />
   				
    <br />
   				Объединение саморегулируемых организаций приобретает статус 				национального объединения саморегулируемых организаций в области 				пожарной безопасности (далее &ndash; национальное объединение) со дня 				включения сведений о нем как о национальном объединении, в 				государственный реестр саморегулируемых организаций в области 				пожарной безопасности органом надзора за саморегулируемыми 				организациями.
    <br />
   				
    <br />
   				<strong>Орган надзора за саморегулируемыми организациями включает 				сведения о национальном объединении в государственный реестр 				саморегулируемых организаций в области пожарной безопасности 				течение семи рабочих дней со дня представления в этот орган 				следующих документов:</strong>
    <br />
   				
    <br />
   				заявление о включении сведений о национальном объединении в 				государственный реестр саморегулируемых организаций в области 				пожарной безопасности с приложением решения общего собрания 				участников национального объединения об утверждении указанного 				заявления;
    <br />
   				
    <br />
   				заверенные объединением саморегулируемых организаций копии 				учредительных документов такого объединения;
    <br />
   				
    <br />
   				заверенные объединением саморегулируемых организаций копии 				заявлений каждого члена такого объединения о вступлении в такое 				объединение.
    <br />
   				
    <br />
   				<strong>Орган надзора за саморегулируемыми организациями отказывает в 				присвоении статуса национального объединения в случае, если:</strong>
    <br />
   				некоммерческой организацией представлены не все документы, 				предусмотренные частью четвертой настоящей статьи;
    <br />
   				
    <br />
   				в государственный реестр саморегулируемых организаций в области 				пожарной безопасности включены сведения об ином национальном 				объединении.
    <br />
   				
    <br />
   				<strong>Основными функциями национального объединения являются:</strong>
    <br />
   				выработка предложений по вопросам государственной политики в 				области пожарной безопасности;
    <br />
   				
    <br />
   				представление интересов саморегулируемых организаций в органах 				государственной власти; 				
