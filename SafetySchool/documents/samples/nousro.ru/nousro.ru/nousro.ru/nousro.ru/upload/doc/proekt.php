    <br />
  
    <br />
   				Порядок деятельности, основные цели, функции, права и 				обязанности саморегулируемой организации определяются в 				соответствии с порядком, установленным Федеральным законом от 1 				декабря 2007 года № 315-ФЗ &laquo;О саморегулируемых организациях&raquo; и 				настоящей главой.
    <br />
  
    <br />
  
    <br />
   				<strong>Основными целями саморегулируемой организации являются:</strong>
    <br />
   				предупреждение причинения вреда жизни, здоровью и имуществу 				граждан, имуществу юридических лиц, государственному и 				муниципальному имуществу, окружающей среде, жизни и здоровью 				животных, объектам культурного наследия памятникам истории и 				культуры народов Российской Федерации (далее - вред) при 				выполнении работ и оказании услуг в области пожарной 				безопасности членами саморегулируемых организаций;
    <br />
   				
    <br />
  повышение качества выполнения работ и оказания услуг в области 				пожарной безопасности членами саморегулируемой организации.
    <br />
  
    <br />
  <strong>Статья 39<sup>2</sup>. Приобретение статуса саморегулируемой организации 				и виды саморегулируемых организаций</strong>
    <br />
   				
    <br />
   				Статус саморегулируемой организации, определенных в части второй 				настоящей статьи может приобрести некоммерческая организация, 				основанная на членстве, объединяющая юридических и (или) 				физических лиц, отвечающих требованиям, установленным статьей 				39<sup>6</sup> настоящего Федерального закона и внесенная в государственный 				реестр саморегулируемых организаций.
    <br />
   				
    <br />
   				<strong>В соответствии с требованиями настоящего Федерального закона 				создаются саморегулируемые организации следующих видов:</strong>
    <br />
   				
    <br />
   				- саморегулируемые организации в области обеспечения пожарной 				безопасности объектов защиты;
    <br />
   				
    <br />
   				- саморегулируемые организации в области экспертизы и оценки 				пожарного риска;
    <br />
   				саморегулируемые организации в области производства 				пожарно-технической продукции.
    <br />
   				
    <br />
   				Иные саморегулируемые организации в области пожарной 				безопасности могут быть созданы в соответствии с порядком, 				установленным Федеральным законом от 1 декабря 2007 года № 				315-ФЗ &laquo;О саморегулируемых организациях&raquo;.
    <br />
   				
    <br />
   				Внесение сведений в государственный реестр саморегулируемых 				организаций в области пожарной безопасности и осуществление 				функций государственного контроля (надзора) за деятельностью 				саморегулируемых организаций осуществляются федеральным органом 				исполнительной власти, уполномоченным на решение задач в области 				обеспечения пожарной безопасности (далее &ndash; орган надзора за 				саморегулируемыми организациями), в соответствии со статьей 20 				Федерального закона от 1 декабря 2007 года № 315-ФЗ &laquo;О 				саморегулируемых организациях&raquo;, и статьей 39<sup>4</sup> настоящего 				Федерального закона. 
    <br />
   				
    <br />
   				<strong>Статья 39<sup>3</sup>. Субъекты, выполняющие работы и оказывающие услуги 				в области пожарной безопасности</strong>
    <br />
   				
    <br />
   				Выполнение работ и оказание услуг в области пожарной 				безопасности, указанных в частях второй и третьей настоящей 				статьи, должны осуществляться только лицами, являющимися членами 				соответствующей саморегулируемой организации и имеющими выданные 				саморегулируемой организацией свидетельства о членстве в 				саморегулируемой организации.
    <br />
   				
    <br />
   				Саморегулируемые организации в области обеспечения пожарной 				безопасности объектов защиты объединяют юридических лиц и 				индивидуальных предпринимателей, осуществляющих монтаж, 				техническое обслуживание и ремонт систем и средств 				противопожарной защиты и выполняющих огнезащитные и трубо-печные 				работы.
    <br />
   				
    <br />
   				Саморегулируемые организации в области экспертизы и оценки 				пожарного риска объединяют физических лиц, осуществляющих 				проведение экспертизы специальных технических условий на 				проектирование и строительство, проектной документации и 				объектов защиты в целях определения соответствия их требованиям 				пожарной безопасности, проектных работ, независимой оценки 				пожарного риска, расчетов по оценке пожарного риска.
    <br />
   				
    <br />
   				Члены саморегулируемой организации вправе выполнять работы и 				оказывать услуги в области пожарной безопасности, указанные в 				частях второй и третьей настоящей статьи, без дополнительных 				разрешительных документов (лицензий, допусков).
    <br />
   				
    <br />
   				Юридические лица и индивидуальные предприниматели, 				осуществляющие производство, закупку и поставку 				пожарно-технической продукции, ремонт и обслуживание пожарного 				снаряжения, первичных средств тушения пожаров, восстановление 				качества огнетушащих средств, вправе добровольно объединяться в 				саморегулируемые организации в области производства 				пожарно-технической продукции, соответствующие требованиям 				настоящего Федерального закона. 
    <br />
   				
    <br />
   				Физические лица, являющиеся членами саморегулируемых организаций 				в области экспертизы и оценки пожарного риска (далее - эксперты 				в области пожарной безопасности), вправе осуществлять 				деятельность самостоятельно, занимаясь частной практикой, а 				также на основании трудового договора между экспертом в области 				пожарной безопасности и юридическим лицом, определенным частью 				седьмой настоящей статьи. 
    <br />
   				
    <br />
   				<strong>Юридическое лицо, которое намерено заключить с заказчиком 				договор на оказание услуг по испытанию веществ, материалов, 				изделий, оборудования и конструкций на пожарную безопасность, а 				также договор на выполнение работ и оказание услуги, являющихся 				предметом деятельности экспертов в области пожарной 				безопасности, обязано:</strong>
    <br />
   				
    <br />
   				- иметь в штате не менее трех экспертов в области пожарной 				безопасности; 				
    <br />
  
    <br />
   				- представлять по требованию заказчика договор страхования 				ответственности эксперта в области пожарной безопасности, 				заключенный в соответствии требованиями настоящего Федерального 				закона;
    <br />
   				
    <br />
   				- предоставлять саморегулируемой организации в области экспертизы 				и оценки пожарного риска, при осуществлении ею контрольных 				полномочий доступ к документам и материалам, на основании 				которых экспертом в области пожарной безопасности, проводилась 				экспертиза, за исключением информации, составляющей коммерческую 				тайну юридического лица или заказчика, либо иной 				конфиденциальной информации. 
    <br />
   				
    <br />
   				<strong>Статья 39<sup>4</sup>. Требования к некоммерческой организации, 				необходимые для приобретения статуса саморегулируемой 				организации</strong>
    <br />
   				
    <br />
   				<strong>Для включения некоммерческой организации в государственный 				реестр саморегулируемых организаций в области пожарной 				безопасности, такая некоммерческая организация должна 				соответствовать требованиям, установленным Федеральным законом 				от 1 декабря 2007 года № 315-ФЗ &laquo;О саморегулируемых 				организациях&raquo;, разработать и принять документы, предусмотренные 				статьей 39<sup>5</sup> настоящего Федерального закона, а также 				соответствовать следующим дополнительным повышенным требованиям:</strong>
    <br />
   				
    <br />
   				для саморегулируемых организаций в области обеспечения пожарной 				безопасности объектов защиты - объединение в составе 				некоммерческой организации в качестве ее членов не менее 				пятидесяти юридических лиц, соответствующих требованиям, 				установленным настоящим Федеральным законом к членству в такой 				саморегулируемой организации, наличие компенсационного фонда, 				сформированного в размере не менее чем пятьдесят тысяч рублей на 				одного члена некоммерческой организации, а также наличие у 				каждого члена саморегулируемой организации договора страхования 				ответственности при осуществлении деятельности в области 				пожарной безопасности, размер страховой суммы по которому не 				может быть менее чем пятьсот тысяч рублей;
    <br />
   				
    <br />
   				для саморегулируемых организаций в области экспертизы и оценки 				пожарного риска - объединение в составе некоммерческой 				организации в качестве ее членов не менее ста пятидесяти 				физических лиц, соответствующих требованиям, установленным 				настоящим Федеральным законом к членству в такой 				саморегулируемой организации, наличие компенсационного фонда, 				сформированного в размере не менее чем двадцать пять тысяч 				рублей на одного члена некоммерческой организации, а также 				наличие у каждого члена саморегулируемой организации договора 				страхования ответственности при осуществлении деятельности в 				области пожарной безопасности, размер страховой суммы по 				которому не может быть менее чем сто тысяч рублей;
    <br />
   				
    <br />
   				для саморегулируемых организаций в области производства 				пожарно-технической продукции - объединение в составе 				некоммерческой организации в качестве ее членов не менее 				двадцати пяти индивидуальных предпринимателей и (или) 				юридических лиц, наличие компенсационного фонда, сформированного 				в размере не менее чем сто тысяч рублей на одного члена 				некоммерческой организации, а также наличие у каждого члена 				саморегулируемой организации договора страхования 				ответственности при осуществлении деятельности в области 				пожарной безопасности, размер страховой суммы по которому не 				может быть менее чем семьсот пятьдесят тысяч рублей.
    <br />
   				
    <br />
   				<strong>Статья 39<sup>5</sup>. Документы саморегулируемой организации</strong>
    <br />
   				
    <br />
   				<strong>Саморегулируемая организация обязана разработать и утвердить:</strong>
    <br />
   				
    <br />
   				условия членства в саморегулируемой организации &ndash; документ, 				устанавливающий условия приема в члены саморегулируемой 				организации, требования к членам саморегулируемой организации и 				условия исключения из членов саморегулируемой организации;
    <br />
   				
    <br />
   				стандарты саморегулируемой организации - документ, 				устанавливающий в соответствии с законодательством Российской 				Федерации о техническом регулировании правила выполнения работ и 				оказания услуг в области пожарной безопасности, к результатам 				выполнения указанных работ и оказания услуг в области пожарной 				безопасности, системе контроля за выполнением указанных работ равила контроля саморегулируемой организации &ndash; документ, 				устанавливающий правила контроля за соблюдением членами 				саморегулируемой организации условий членства в саморегулируемой 				организации, требований стандартов саморегулируемой организации;
